<template><div><masthead title="Width" description="Use these utility classes to set element widths." /><h2 id="how-it-works" tabindex="-1"><a class="header-anchor" href="#how-it-works" aria-hidden="true">#</a> How It Works</h2>
<p>These classes can be applied using the following format <br/>
<code v-pre>u-{value1}/{value2}</code></p>
<ul>
<li><code v-pre>value2</code> can be <code v-pre>2</code>, <code v-pre>3</code>, <code v-pre>4</code>, <code v-pre>5</code>, <code v-pre>6</code>, <code v-pre>8</code>, <code v-pre>12</code> &amp; <code v-pre>24</code> .</li>
<li><code v-pre>value1</code> can have the value from <code v-pre>1</code> to max value of <code v-pre>value2</code> .</li>
</ul>
<p>Examples:</p>
<ul>
<li><code v-pre>u-1/2</code> or <code v-pre>u-2/2</code> - Halves</li>
<li><code v-pre>u-1/3</code> or <code v-pre>u-3/3</code> - Thirds</li>
<li><code v-pre>u-1/4</code> or <code v-pre>u-3/4</code> - Fourths</li>
<li><code v-pre>u-1/8</code> or <code v-pre>u-3/8</code> - Eighths</li>
<li><code v-pre>u-1/12</code> or <code v-pre>u-3/12</code> - Twelfths</li>
<li><code v-pre>u-1/24</code> or <code v-pre>u-3/24</code> - Twenty-fourths</li>
</ul>
</div></template>


